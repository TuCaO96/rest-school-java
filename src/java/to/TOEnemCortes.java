public class TOEnemCortes {
    private int id;
    private int ano;
    private int nota_maxima;
    private int nota_minima;
    private String curso;

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the ano
     */
    public int getAno() {
        return ano;
    }

    /**
     * @param ano the ano to set
     */
    public void setAno(int ano) {
        this.ano = ano;
    }

    /**
     * @return the nota_maxima
     */
    public int getNota_maxima() {
        return nota_maxima;
    }

    /**
     * @param nota_maxima the nota_maxima to set
     */
    public void setNota_maxima(int nota_maxima) {
        this.nota_maxima = nota_maxima;
    }

    /**
     * @return the nota_minima
     */
    public int getNota_minima() {
        return nota_minima;
    }

    /**
     * @param nota_minima the nota_minima to set
     */
    public void setNota_minima(int nota_minima) {
        this.nota_minima = nota_minima;
    }

    /**
     * @return the curso
     */
    public String getCurso() {
        return curso;
    }

    /**
     * @param curso the curso to set
     */
    public void setCurso(String curso) {
        this.curso = curso;
    }
    
    
}
